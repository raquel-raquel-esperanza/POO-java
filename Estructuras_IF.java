public class Estructuras_IF {

    public static void main(String[] args) {

        String name = "Rosa";
        int edad = 7;
   

        if (edad > 17) {
            System.out.println("Edad de " + name + " es: " + edad + "; es mayor de edad");
        } else if (edad <= 17) {
            System.out.println("Edad de " + name + " es: " + edad + "; es menor de edad");
        } 
    }


}

        